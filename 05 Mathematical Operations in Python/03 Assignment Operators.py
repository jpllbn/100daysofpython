# Assignment Operators
# Assignment operators are used to assign values to variables

# =
a = 10
print(a)

# +=
a += 2
print(a)

# -=
a -= 5
print(a)

# *=
a *= 5
print(a)

# /=
a /= 2
print(a)

# %=
a %= 2
print(a)

# **=
a **= 10
print(a)

# //=
a //= 3
print(a)

# &=
a = 5
a &= 3
print(a)

# |=
a |= 10
print(a)

# ^=
a ^= 3
print(a)

# >>=
a >>= 3
print(a)

# <<= 3
a <<= 3
print(a)
