# Python Comparison Operators
# Comparison operators are used to compare two values

x = 5
y = 8

# ==
z = x == y
print(z)

# != not equal
z = x != y
print(z)

# > greater than
z = x > y
print(z)

# < less than
z = x < y
print(z)

# >= greater than equal to
z = x >= y
print(z)

# <= less than equal to
z = x <= y
print(z)

