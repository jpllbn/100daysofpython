# Arithmitic Operators
# Arithmetic operators are used with numeric values to perform common mathematical operations
x = 5
y = 7

# Addition
print(x + y)

# Subtraction
print(x - y)

# Multiplication
print(x * y)

# Division
print(x / y)

# Modulus
print(x % y)

# Exponentation
print(x ** y)

# Floor Division
# The floor division // rounds the result down to the nearest whole number
print(x // y)
